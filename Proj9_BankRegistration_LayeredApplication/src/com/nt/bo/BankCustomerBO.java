package com.nt.bo;

public class BankCustomerBO {
	private String accHolder;
	private String addr;
	private long mobNo;
	private float initAmt;
	private float finalAmt;
	private String accType;
	
	public String getAccHolder() {
		return accHolder;
	}
	public void setAccHolder(String accHolder) {
		this.accHolder = accHolder;
	}
	public String getAddr() {
		return addr;
	}
	public void setAdd(String addr) {
		this.addr = addr;
	}
	public long getMobNo() {
		return mobNo;
	}
	public void setMobNo(long mobNo) {
		this.mobNo = mobNo;
	}
	public float getInitAmt() {
		return initAmt;
	}
	public void setInitAmt(float initAmt) {
		this.initAmt = initAmt;
	}
	public float getFinalAmt() {
		return finalAmt;
	}
	public void setFinalAmt(float finalAmt) {
		this.finalAmt = finalAmt;
	}
	public String getAccType() {
		return accType;
	}
	public void setAccType(String accType) {
		this.accType = accType;
	}
	
	

}
